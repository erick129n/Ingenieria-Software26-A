class Cliente():
    def __init__(self, id, nombre, direccion, telefono, genero, nacimiento, estatura, peso, imc):
        self.id = id
        self.nombre = nombre
        self.direccion = direccion
        self.telefono = telefono
        self.genero = genero
        self.nacimiento = nacimiento
        self.estatura = estatura
        self.peso = peso
        self.imc = imc
